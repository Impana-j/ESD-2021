 class Ascii {
	public static void main(String[] args) 
	{
		int i;
		
		for(i = 65; i <= 90; i++)
		{
			System.out.println(" The ASCII value of " + (char)i + "  =  " + i);
		}
		
		for(i = 97; i <= 122; i++)
		{
			System.out.println(" The ASCII value of " + (char)i + "  =  " + i);
		}
		
		for(i = 48; i <= 57; i++)
		{
			System.out.println(" The ASCII value of " + (char)i + "  =  " + i);
		}
	}
}